import { useStaticQuery, graphql } from "gatsby"
export const usePowertoolsProducts = () => {
  const { allPowertoolsProduct } = useStaticQuery(
    graphql`
      {
        allPowertoolsProduct {
          edges {
            node {
              code
              name
              price {
                value
              }
              images {
                format
                imageType
                url
              }
            }
          }
        }
      }
    `
  )
  return allPowertoolsProduct.edges
}
